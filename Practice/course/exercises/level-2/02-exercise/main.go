//Using the following operators, write expressions and assign their values to variables:
//1. ==
//2. <=
//3. >=
//4. !=
//5. <
//6. >

package main

import "fmt"

func main()  {
	g := 100 == 100
	h :=  100 <= 11
	i := 6 >= 5
	j := 5 != 5
	k := 1000 < 100000
	l := 10 > 1
	fmt.Println("g: ", g)
	fmt.Println("h: ", h)
	fmt.Println("i: ", i)
	fmt.Println("j: ", j)
	fmt.Println("k: ", k)
	fmt.Println("l: ", l)
}